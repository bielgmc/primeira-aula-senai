aumento_percentual = 0.015  # 1.5% em 1995
ano_atual = 2025

salario_atual = salario_inicial

print(f"Salário em 1995: R$ {salario_atual:.2f}")

for ano in range(1996, ano_atual + 1):
  aumento_percentual *= 2
  salario_atual += salario_atual * aumento_percentual
  print(f"Salário em {ano}: R$ {salario_atual:.2f}")

