# prompt: 19.Durante 7 dias, a cidade registrou temperaturas. Mostre a temperatura de
# cada dia, a média, a maior e a menor.

temperaturas = []
for dia in range(1, 8):
  temp = int(input(f"Quantos graus está no dia {dia}? "))
  temperaturas.append(temp)

media = sum(temperaturas) / len(temperaturas)
maior = max(temperaturas)
menor = min(temperaturas)

print("\nTemperaturas por dia:")
for i, temp in enumerate(temperaturas):
  print(f"Dia {i+1}: {temp} graus")

print(f"\nMédia da temperatura: {media:.2f} graus")
print(f"Maior temperatura registrada: {maior} graus")
print(f"Menor temperatura registrada: {menor} graus")

